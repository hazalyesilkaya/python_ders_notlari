import math

# üslü kuvvet alma
# print(pow(2,3))
kuvvet = pow(3, 5)
print(kuvvet)

# mutlak değer alma
mutlak = abs(-123)
print(mutlak)

# en yakına yuvarlama
yuvarla = round(-12.6)
print(yuvarla)

# üst değere yuvarlama
tavan = math.ceil(12.25)
print(tavan)

# alt değere yuvarlama
taban = math.floor(12.59)
print(taban)

#karekök alma
karekok = math.sqrt(26)
print(karekok)

#faktöriyel alma
fakt = math.factorial(5)
print(fakt)

# math.pi ile pi sayısına ulaşılabiliyor.
print(math.pi)
